package urfu.emelianov.dto;

import lombok.Getter;

/**
 * Перечисление {@code Gender} представляет различные половые идентификаторы.
 * <p>Перечисление содержит два элемента: {@code MALE} и {@code FEMALE}, представляющие мужской и
 *  * женский пол соответственно.</p>
 */
@Getter
public enum Gender {
    MALE,
    FEMALE
}
